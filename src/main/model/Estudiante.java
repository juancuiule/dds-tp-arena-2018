package model;

public class Estudiante {

	String nombre;
	String legajo;
	String contraseña;
	String usuarioGithub;

	public Estudiante(String nombre, String legajo, String contraseña, String usuarioGithub) {
		this.nombre = nombre;
		this.legajo = legajo;
		this.contraseña = contraseña;
		this.usuarioGithub = usuarioGithub;
	}

}
