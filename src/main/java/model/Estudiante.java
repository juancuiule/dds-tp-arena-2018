package model;

public class Estudiante {

	String nombre;
	String legajo;
	String password;
	String usuarioGithub;

	public Estudiante(String nombre, String legajo, String password, String usuarioGithub) {
		this.nombre = nombre;
		this.legajo = legajo;
		this.password = password;
		this.usuarioGithub = usuarioGithub;
	}

}